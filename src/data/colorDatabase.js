const CATEGORY_ORDER = ['brands', 'flowers', 'nature', 'food']

export function searchColors(query) {
  const q = query.toLowerCase().trim()
  if (q.length < 2) return []

  const scored = COLOR_DATABASE
    .map((color) => {
      const nameLower = color.name.toLowerCase()
      const nameMatch = nameLower.includes(q) ? (nameLower.startsWith(q) ? 3 : 2) : 0
      const tagMatch = color.tags.some((t) => t.includes(q)) ? 1 : 0
      return { color, score: nameMatch + tagMatch }
    })
    .filter(({ score }) => score > 0)
    .sort((a, b) => b.score - a.score || CATEGORY_ORDER.indexOf(a.color.category) - CATEGORY_ORDER.indexOf(b.color.category))

  return scored.map(({ color }) => color).slice(0, 8)
}
